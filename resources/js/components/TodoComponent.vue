<template>
    <div class="w-50">
        <form @submit.prevent="saveData">
            <div class="input-group mb-3 w-100">
                <input
                    v-model="form.title"
                    type="text"
                    class="form-control form-control-lg"
                    placeholder="Add Todo"
                    aria-label="Add Todo"
                    aria-describedby="button-addon2"
                />
                <div class="input-group-append">
                    <button
                        class="btn btn-success"
                        type="submit"
                        id="button-addon2"
                    >
                        ADD TODO
                    </button>
                </div>
            </div>
        </form>
        <div class="w-25">
            <div 
            v-for="todo in todos" 
            v-bind:key="todo.id"
            class="w-100">
                    {{ todo.title }}
            </div>
        </div>
    </div>
</template>

<script>
export default {
    data: () => {
        return {
            form: new Form({
                title: 'Test',
            }),
            todos: []
        };
    },
    methods: {
        getTodos() {
            axios.get('/api/todo')
            .then((res)=>{
                this.todos = res.data;
                console.log(res.data);
            })
            .catch(error => {
                console.log(error);
            })
        },
        saveData(){
            let data = new FormData(); 
            data.append('title', this.form.title);
   
            axios.post('/api/todo', data)
